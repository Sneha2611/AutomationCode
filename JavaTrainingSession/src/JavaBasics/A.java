package JavaBasics;

public class A {

	
	  public A()
	  { 
		  System.out.println("parent class constructor"); 
	}
	  
	  public A(int i)
	  {
		  System.out.println(i);
	  }
	  
	  public A(int i,int j)
	  {
		  System.out.println(i);
		  System.out.println(j);
	  }
	  
	 
}
